// En tu archivo principal de Electron

const { app, BrowserWindow } = require('electron');
const path = require('path');

let mainWindow;

function createWindow() {
  mainWindow = new BrowserWindow({
    width: 800,
    height: 600,
    webPreferences: {
      nodeIntegration: false,
      contextIsolation: true
    }
  });

  // Carga los archivos estáticos de producción
  const startUrl = path.join(__dirname, 'dist/almacen-front/index.html');
  mainWindow.loadFile(startUrl);

  // Opcional: abre las herramientas de desarrollo solo si es necesario.
  // mainWindow.webContents.openDevTools();
}

app.whenReady().then(createWindow);

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', () => {
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow();
  }
});
